package com.niit.backend.model;

import java.util.*;
public class Login {
	
	private int id;
	private String userMail;
	private String loggedOnDate;
	private String loggedOutDate;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getLoggedOnDate() {
		return loggedOnDate;
	}
	public void setLoggedOnDate(String loggedOnDate) {
		this.loggedOnDate = loggedOnDate;
	}
	public String getLoggedOutDate() {
		return loggedOutDate;
	}
	public void setLoggedOutDate(String loggedOutDate) {
		this.loggedOutDate = loggedOutDate;
	}
	
	
	
	
	
	
	
 
}
